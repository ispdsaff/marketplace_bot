from telegram import Update
from telegram.ext import ContextTypes


async def fallback_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    message = update.effective_message

    if message.text:
        text = message.text.strip()

        # –°–ª–∏—à–∫–æ–º –∫–æ—Ä–æ—Ç–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
        if len(text) < 10:
            await message.reply_text(
                "–°–ø–∞—Å–∏–±–æ! –ù–æ –µ—Å–ª–∏ –ø–æ–ª—É—á–∏—Ç—Å—è ‚Äî –ø—Ä–∏—à–ª–∏ —á—É—Ç—å –±–æ–ª—å—à–µ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ üôè\n"
                "–û–ø–∏—à–∏ —Ç–æ–≤–∞—Ä, –µ–≥–æ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏, –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ ‚Äî —á–µ–º –±–æ–ª—å—à–µ, —Ç–µ–º –ª—É—á—à–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç! üí°"
            )
        # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –æ—Ç–ø—Ä–∞–≤–∏–ª —Å—Å—ã–ª–∫—É
        elif "http://" in text or "https://" in text:
            await message.reply_text(
                "üß© –ü–æ–∫–∞ —è –Ω–µ —É–º–µ—é —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –ø–æ —Å—Å—ã–ª–∫–µ.\n"
                "–ù–∞–ø–∏—à–∏ —Ç–µ–∫—Å—Ç–æ–º, —á—Ç–æ –∑–∞ —Ç–æ–≤–∞—Ä –∏–ª–∏ –æ—Ç–∑—ã–≤ ‚Äî –∏ —è –ø–æ–º–æ–≥—É —Ç–µ–±–µ –ø–æ –ø–æ–ª–Ω–æ–π! üöÄ"
            )
        # –ï—Å–ª–∏ –Ω–µ—Ç –∞–∫—Ç–∏–≤–Ω–æ–≥–æ —Å–æ—Å—Ç–æ—è–Ω–∏—è
        else:
            await message.reply_text(
                "–Ø –ø–æ–∫–∞ –Ω–µ –ø–æ–Ω—è–ª, —á—Ç–æ —Ç—ã —Ö–æ—á–µ—à—å —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å ü§ñ\n"
                "–í—ã–±–µ—Ä–∏ –¥–µ–π—Å—Ç–≤–∏–µ –≤ –º–µ–Ω—é –∏ –æ—Ç–ø—Ä–∞–≤—å –Ω—É–∂–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –µ—â—ë —Ä–∞–∑.",
            )

    # –§–æ—Ç–æ / –¥–æ–∫—É–º–µ–Ω—Ç
    elif message.photo or message.document:
        await message.reply_text(
            "üñºÔ∏è –ö–∞—Ä—Ç–∏–Ω–∫–∏ —è –ø–æ–∫–∞ –Ω–µ —Ä–∞—Å–ø–æ–∑–Ω–∞—é (–Ω–∞ —Å—Ç–∞–¥–∏–∏ MVP).\n"
            "–û–ø–∏—à–∏ —Ç–æ–≤–∞—Ä —Å–ª–æ–≤–∞–º–∏ ‚Äî –∏ —è –ø–æ–¥–≥–æ—Ç–æ–≤–ª—é —Ç–µ–∫—Å—Ç –Ω–µ —Ö—É–∂–µ —Ç–æ–ø-—Å–µ–ª–ª–µ—Ä–æ–≤!"
        )

    # –ì–æ–ª–æ—Å–æ–≤–æ–µ / –∞—É–¥–∏–æ
    elif message.voice or message.audio:
        await message.reply_text(
            "üéôÔ∏è –ì–æ–ª–æ—Å–æ–≤—ã–µ –ø–æ–∫–∞ –≤–Ω–µ –∑–æ–Ω—ã –¥–æ—Å—Ç—É–ø–∞ üòÖ\n"
            "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –Ω–∞–ø–∏—à–∏ –æ–ø–∏—Å–∞–Ω–∏–µ –∏–ª–∏ –æ—Ç–∑—ã–≤ –≤ —Ç–µ–∫—Å—Ç–æ–≤–æ–º –≤–∏–¥–µ."
        )

    # –í—Å—ë –æ—Å—Ç–∞–ª—å–Ω–æ–µ
    else:
        await message.reply_text(
            "–ù–µ —Å–æ–≤—Å–µ–º –ø–æ–Ω—è–ª‚Ä¶ üò∂ –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –æ—Ç–ø—Ä–∞–≤—å —Ç–µ–∫—Å—Ç–æ–≤–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∏–ª–∏ –≤–æ—Å–ø–æ–ª—å–∑—É–π—Å—è –∫–Ω–æ–ø–∫–∞–º–∏ –º–µ–Ω—é."
        )