def generate_title_prompt(product_info: str, marketplace: str) -> str:
    if marketplace == "wildberries":
        return (
            f"Придумай SEO-название для товара на Wildberries. "
            f"Название должно быть релевантным, включать ключевые слова и быть понятным покупателю.\n\n"
            f"Информация о товаре: {product_info}\n\n"
            f"Выдай 1–2 варианта, без маркировки пунктов. Без кавычек, просто текст."
        )
    else:  # ozon
        return (
            f"Сгенерируй эффективное SEO-название для товара на Ozon. "
            f"Название должно содержать ключевые слова и выделять товар среди конкурентов.\n\n"
            f"Информация о товаре: {product_info}\n\n"
            f"Выдай 1–2 варианта, без кавычек, без нумерации."
        )


def generate_description_prompt(product_info: str, marketplace: str) -> str:
    if marketplace == "wildberries":
        return (
            f"Напиши длинное SEO-описание для товара на Wildberries. "
            f"Описание должно начинаться с ключевых слов, быть читаемым, убедительным и подходить под алгоритмы WB.\n\n"
            f"Описание товара: {product_info}\n\n"
            f"Выдай 1 связный абзац. Без маркировок и списков."
        )
    else:  # ozon
        return (
            f"Создай SEO-оптимизированное описание для товара на Ozon. "
            f"Описание должно быть информативным, содержать ключевые фразы и вызывать доверие у покупателя.\n\n"
            f"Информация о товаре: {product_info}\n\n"
            f"Сформулируй как единый абзац. Без списков."
        )